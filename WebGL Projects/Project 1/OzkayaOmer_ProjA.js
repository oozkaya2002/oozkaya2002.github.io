function myMouseDown(ev) {
// Called when user PRESSES down any mouse button; 
    
    //Create right-handed 'pixel' coords with origin at WebGL canvas LOWER left;
    var rect = ev.target.getBoundingClientRect();	// get canvas corners in pixels
    var xp = ev.clientX - rect.left;									// x==0 at canvas left edge
    var yp = g_canvas.height - (ev.clientY - rect.top);	// y==0 at canvas bottom edge
    // console.log('myMouseDown(pixel coords): xp,yp=\t',xp,',\t',yp);    


    // Convert to Canonical View Volume (CVV) coordinates to:
       //move origin to center of canvas and normalize canvas to -1 <= x < +1, -1 <= y < +1.
    var x = (xp - g_canvas.width/2)/(g_canvas.width/2);
    var y = (yp - g_canvas.height/2)/(g_canvas.height/2);
    //	console.log('myMouseDown(CVV coords  ):  x, y=\t',x,',\t',y);
        
    //Set our mouse-dragging flag
    g_isDrag = true;

    // Record where mouse-dragging began
    g_xMclik = x;
    g_yMclik = y;
    
    //Report on webpage
    document.getElementById('MouseAtResult').innerHTML = 'Mouse At: '+x.toFixed(g_digits)+', '+y.toFixed(g_digits);
};

function myMouseMove(ev) {
    // Called when user MOVES the mouse with a button already pressed down.
    //(Which button?   console.log('ev.button='+ev.button);    )
    //ev.clientX, ev.clientY == mouse pointer location, but measured in webpage 
    //pixels: left-handed coords; UPPER left origin; Y increases DOWNWARDS (!)  

    //IGNORE all mouse-moves except 'dragging'
    if(g_isDrag==false) return;

    // Create right-handed 'pixel' coords with origin at WebGL canvas LOWER left;
        // get canvas corners in pixels
        // x==0 at canvas left edge
    var rect = ev.target.getBoundingClientRect();
    var xp = ev.clientX - rect.left;

    // y==0 at canvas bottom edge
    var yp = g_canvas.height - (ev.clientY - rect.top);

    //console.log('myMouseMove(pixel coords): xp,yp=\t',xp,',\t',yp);
    
    //Convert to Canonical View Volume (CVV) coordinates too:
        // move origin to center of canvas and normalize canvas to -1 <= x < +1, -1 <= y < +1.
    var x = (xp - g_canvas.width/2)/(g_canvas.width/2);		
    var y = (yp - g_canvas.height/2)/(g_canvas.height/2);
    //console.log('myMouseMove(CVV coords  ):  x, y=\t',x,',\t',y);

    // find how far we dragged the mouse:
    // Accumulate change-in-mouse-position
    g_xMdragTot += (x - g_xMclik);
    g_yMdragTot += (y - g_yMclik);

    // Report new mouse position & how far we moved on webpage:
    document.getElementById('MouseAtResult').innerHTML = 'Mouse At: '+x.toFixed(g_digits)+', '+y.toFixed(g_digits);
    document.getElementById('MouseDragResult').innerHTML = 'Mouse Drag: '+(x - g_xMclik).toFixed(g_digits)+', ' +(y - g_yMclik).toFixed(g_digits);

    // Make next drag-measurement from here.
    g_xMclik = x;
    g_yMclik = y;
};

