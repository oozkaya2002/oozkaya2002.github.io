function main() {

    //Get gl from canvas obj
    gl = getWebGLContext(g_canvas);
    if(!gl){
        console.log('Failed to get the rendering context for WebGL');
        return;
    }

    //Shader Init
    if(!initShaders(gl, VSHADER_SOURCE, FSHADER_SOURCE)){
        console.log('Failed to intialize shaders.');
        return;
    }

    //Init Vertex Buffer to hold Vertices
    g_maxVerts = initVertexBuffer(gl);
    if(g_maxVerts < 0){
        console.log('Failed to set the vertex information');
        return;
    }

    //Mouse and KB Tings--
    
    //KB:
    window.addEventListener("keydown", myKeyDown, false);
    window.addEventListener("keyup", myKeyUp, false);
    
    //Mouse:
    window.addEventListener("mousedown", myMouseDown);
    window.addEventListener("mousemove", myMouseMove);
    window.addEventListener("mouseup", myMouseUp);
    window.addEventListener("click", myMouseClick);		
    window.addEventListener("dblclick", myMouseDblClick);

    //Canvas Color
    gl.clearColor(0.25, 0.25, 0.25, 1.0);

    //Enable 3D Depth-test
    gl.depthFunc(gl.LESS);
	gl.enable(gl.DEPTH_TEST);

    //Get handle to graphics system's storage location of u_ModelMatrix
    g_modelMatLoc = gl.getUniformLocation(gl.program, 'u_ModelMatrix');
    if(!g_modelMatLoc){ 
        console.log('Failed to get the storage location of u_ModelMatrix');
        return;
    }

    var tick = function(){
        animateDrill();
        drawDrill();


        drawStar();
        animateStar();
        //Display current angle_1 value
        //document.getElementById('CurAngleDisplay').innerHTML =
            //'angle_1 = ' + drill_angle.toFixed(g_digits);
        
        //Display current mouse-dragging state
        document.getElementById('Mouse').innerHTML = 
            'Mouse Drag totals (CVV coords):\t' +
             g_xMdragTot.toFixed(5)+'. \t' + g_yMdragTot.toFixed(g_digits);
        
        requestAnimationFrame(tick, g_canvas);
    };

    tick();

}

